public class Car {

    private String plate;
    private String id;

    public Car(String plate, String id) {
        this.plate = plate;
        this.id = id;
    }

    public String getPlate() {
        return plate;
    }

    public void setPlate(String plate) {
        this.plate = plate;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
